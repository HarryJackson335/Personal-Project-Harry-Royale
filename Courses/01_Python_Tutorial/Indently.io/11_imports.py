# Importing the whole math module
import math

# Importing the math module with an alias
import math as m

# Importing specific function(s) from the math module
from math import sqrt, tan

# Using the math module directly
print(math.sqrt(3))

# Using the math module with an alias
print(m.sqrt(4))

# Using the imported specific function
print(sqrt(5))

# Using another imported specific function
print(tan(2))